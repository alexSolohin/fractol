#include "libft.h"

void	ft_printbits(unsigned char c)
{
	int i;

	i = 128;
	while (i > 0)
	{
		if (c - i >= 0)
		{
			write(1, "1", 1);
			c = c - i;
			i = i / 2;
		}
		else
		{
			write(1, "0", 1);
			i = i / 2;
		}
	}
}
